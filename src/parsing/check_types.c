#include "../../inc/minishell.h"

int	is_operator(char c, char d)
{
	if (c == d && (c == '|' || c == '&' || c == '<' || c == '>'))
		return (2);
	else if (((c == '|' && d != '|') || (c == '<' && d != '<')
			|| (c == '>' && d != '>') || c == '(' || c == ')'))
		return (1);
	return (0);
}

int	is_space(char c)
{
	if (c == ' ' || c == '\t' || c == '\0')
		return (1);
	return (0);
}
