#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	nbr;
	char	ch;

	nbr = n;
	if (nbr < 0)
	{
		write(fd, "-", 1);
		nbr = -nbr;
	}
	if (nbr >= 10)
	{
		ft_putnbr_fd(nbr / 10, fd);
		ch = 48 + nbr % 10;
		write(fd, &ch, 1);
	}
	else
	{
		ch = 48 + nbr;
		write(fd, &ch, 1);
	}
}
