#include "../libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*successor;
	int		len;

	len = 0;
	while (lst != NULL)
	{
		successor = lst->next;
		lst = successor;
		len++;
	}
	return (len);
}
