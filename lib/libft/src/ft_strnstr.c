#include "../libft.h"

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
{
	size_t	i;
	size_t	j;
	size_t	len_j;

	if (!haystack && len == 0)
		return (NULL);
	i = 0;
	len_j = 0;
	while (needle[len_j] != '\0')
		len_j++;
	if (len_j == 0)
		return ((char *)haystack);
	while (haystack[i] != '\0' && i < len)
	{
		j = 0;
		while (haystack[i + j] == needle[j] && needle[j] && i + j < len)
			j++;
		if (j == len_j)
			return ((char *)(&haystack[i]));
		i++;
	}
	return (NULL);
}
