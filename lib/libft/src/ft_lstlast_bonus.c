#include "../libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*successor;
	t_list	*current;

	current = lst;
	if (lst == NULL)
		return (NULL);
	else
	{
		while (current->next != NULL)
		{
			successor = current->next;
			current = successor;
		}
	}
	return (current);
}
